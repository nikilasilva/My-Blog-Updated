@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        new_user = User(
            email=form.email.data,
            name=form.name.data,
            password=generate_password_hash(password=form.password.data, method="pbkdf2:sha256", salt_length=8)
        )
        db.session.add(new_user)
        db.session.commit()

        # Login and authenticate user after data added to db.
        login_user(new_user)

        return redirect(url_for('get_all_posts'))
    # current_user.is authenticated is a flask-login built in method to recognize if the user logged in.
    return render_template("register.html", form=form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=["GET", "POST"])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        email = login_form.email.data
        password = login_form.password.data

        user = User.query.filter_by(email=email).first()

        if not user:
            flash("The email does not exists. Please try again.")
            return redirect(url_for('login'))

        elif not check_password_hash(pwhash=user.password, password=password):
            flash("The password is incorrect. Please try again.")
            return redirect(url_for('login'))
        else:
            login_user(user)
            return redirect(url_for('get_all_posts'))

    return render_template("login.html", form=login_form, logged_in=current_user.is_authenticated)


